    /** Esta função é responsável por consultar o usuário no banco e validar o logi
     * @return false se não fizer login ou se o usuário for um instrutors
     */
    public boolean doLogin(){
        //VERIFICA SE FORAM DIGITADOS OS DADOS
        if (txtUsuario.getText().isEmpty() || txtSenha.getPassword().length == 0)
            return false;
        
        /* Verifica se o usuário que está logando é um instrutor ou operador comum */
        controller = new UsuarioController();
        String senha = "";
        for (char c : txtSenha.getPassword()) {
            senha += c;
        }
        
        usuario = controller.login(txtUsuario.getText(),senha);
        //se fizer login
        if (usuario != null){
            
            IS_LOGGED = true;
            dispose();
        } else {    
            //se não manda mensagem de erro
            ShowMessage.warningMessage("Usuario/Senha incorretos!");
            txtUsuario.setText("");
            txtSenha.setText("");
            txtUsuario.requestFocus();
            IS_LOGGED = false;
        }//fim se fizer login
        
        return IS_LOGGED;
    }//fim doLogin
    
