    private void loadServicos(){
        
        MyTableModel dtm = new MyTableModel();
        dtm.setColunas(new String[]{"Nome","Valor","Ativo"});
        for (Servico c : controller.findByParameters("nome", "", Comparadores.IGUAL) ){
            dtm.addRow(new Object[]{c.getNome()
                    ,DecimalFormat.getCurrencyInstance().format(c.getValor())
                    ,c.isAtivo()});
            
        }
        tblServicos.setModel(dtm);
    }

