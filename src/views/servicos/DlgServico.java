    @Override
    public void excluir() {
        if (tblServicos.getSelectedRow() < 0){
            ShowMessage.informationMessage("Selecione ao menos um convênio!");
            return;
        }
        int msg = ShowMessage.confirmMessage("Tem certeza que deseja excluir este registro?"
                + "\nA exclusão do mesmo pode afetar outros registros no sistema.");
        if (msg == 0){
            controller.setServico(servico);
            if (controller.delete()){
                ShowMessage.informationMessage("O convênio foi excluido!");
                novo();
                cancelar();
                loadServicos();
            }//fim delete
        }//fim msg==0
    }

    @Override
    public void localizar() {
        
        if (tblServicos.getSelectedRow() >= 0){
            int index = tblServicos.getRowSorter().convertRowIndexToModel(tblServicos.getSelectedRow());
            String nome = (String) tblServicos.getValueAt(index, 0);
            setDataForm(controller.findParameters("nome",nome,Comparadores.IGUAL));
            enableControls(false);
            btnEditar.setEnabled(true);
            btnNovo.setEnabled(true);
            btnExcluir.setEnabled(true);
            IS_EDITAR = true;
        }//fim 
    }

